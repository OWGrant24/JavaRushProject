package Java_Multithreading.Multithreading4;

public class Level24Task04_Point {
    private double x;
    private double y;

    public Level24Task04_Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}

